    public void show_rutas(){
        String query = "SELECT * FROM tb_rutas";
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(query);
            Object[] ruta = new Object[3];
            model = (DefaultTableModel)tbl_rutas.getModel();
            while(rs.next()){
                ruta[0] = rs.getInt("kilometrje");
                ruta[1] = rs.getString("salida");
                ruta[2] = rs.getString("llegada");
                model.addRow(ruta);
            }
            tbl_rutas.setModel(model);
        }catch(SQLException e){
            
        }
    }
    
    public void add_ruta(){
        String salida = txt_salida.getText();
        String llegada = txt_destino.getText();
        int horaLlegada = Integer.parseInt(txt_horaLlegada.getText());
        int horaSalida = Integer.parseInt(txt_horaSalida.getText());
        int km = Integer.parseInt(txt_kilometraje.getText());
        String fecha = txt_fecha.getText();
        
        if (salida.isEmpty() || llegada.isEmpty() || llegada.isEmpty() ) {
            JOptionPane.showMessageDialog(this, "Faltan campos por diligenciar");
        } else {
            
                String query = "INSERT INTO tb_rutas(salida, destino, kilometraje, fecha, horaSalida, horaLlegada)VALUES('" + salida + "','" + llegada + "','" + km + "','" + fecha + "','" + horaSalida + "','" + horaLlegada + "')";
            try {
                cn = con.getConnection();
                st = cn.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Se ha creado el registro con Ã©xito");
                clear_rows_table();
                show_rutas();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "No se pudo crear el registro");
            }
            
            
        }
    }
    public void clear_rows_table(){
        for (int i = 0; i < tbl_rutas.getRowCount(); i++) {
            model.removeRow(i);
            i -= 1;
        }
        

    }

    
